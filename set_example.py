# Membuat Set Angka
angka = {1, 2, 2, 4, 5}
print("Set Angka:", angka, "\n")

# Menambah Angka Baru
angka.add(7)
print("Setelah ditambah 6:", angka, "\n")

# Menghapus Angka 
angka.remove(4)
print("Setelah menghapus 4:", angka, "\n")

# Mengecek apakah angka ada di set
print("Apakah angka 4 ada?", 4 in angka)
